public class switchCase {
    
    public static void main(String args[]){

        int ch = 2;

        switch(ch){

            case 1 : System.out.println("BURGERRR!");
                            break;
            case 2 : System.out.println("PIZAAAA!");
                            break;
            case 3 : System.out.println("MAGGIEEE!");
                            break;
            default : System.out.println("EORROR BROO!");
        }
    }
}
